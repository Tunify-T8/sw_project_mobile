/// Spacing tokens used for padding and gaps throughout the app.
///
/// Use these constants instead of hard-coded values to keep
/// spacing consistent and easy to adjust globally.
class AppSpacing {
  AppSpacing._();

  /// 4 dp — micro gap.
  static const double xs = 4.0;

  /// 8 dp — small gap.
  static const double sm = 8.0;

  /// 12 dp — medium-small gap.
  static const double md = 12.0;

  /// 16 dp — base gap (most common).
  static const double base = 16.0;

  /// 20 dp — medium gap.
  static const double lg = 20.0;

  /// 24 dp — large gap.
  static const double xl = 24.0;

  /// 32 dp — extra-large gap.
  static const double xxl = 32.0;

  /// 40 dp — section gap.
  static const double xxxl = 40.0;

  /// Standard horizontal screen padding.
  static const double screenHorizontal = 24.0;

  /// Standard bottom safe-area padding.
  static const double screenBottom = 40.0;
}
